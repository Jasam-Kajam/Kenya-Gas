export const kenyaCounties = Object.keys(townsByCounty).sort();

// ======================================================
// HELPER FUNCTIONS
// ======================================================

export function getTowns(county) {
    return townsByCounty[county] || [];
}

export function countyExists(county) {
    return Object.prototype.hasOwnProperty.call(townsByCounty, county);
}

export function searchLocations(keyword) {

    if (!keyword) return [];

    const search = keyword.toLowerCase();

    const results = [];

    for (const county of kenyaCounties) {

        for (const town of townsByCounty[county]) {

            if (town.toLowerCase().includes(search)) {

                results.push({
                    county,
                    town
                });

            }

        }

    }

    return results;

}

